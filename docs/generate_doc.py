# ===================== ЖАРДАМЧЫ ФУНКЦИЯЛАР =====================
def _runs(p, text, bold=False, italic=False, size=SZ):
    r = p.add_run(text)
    r.bold = bold
    r.italic = italic
    r.font.name = FONT
    r.font.size = Pt(size)
    r.element.rPr.rFonts.set(qn("w:eastAsia"), FONT)
    return r


def para(text="", bold=False, italic=False, size=SZ, align="just",
         indent=True, before=0, after=6, spacing=1.5):
    p = doc.add_paragraph()
    al = {"just": WD_ALIGN_PARAGRAPH.JUSTIFY, "center": WD_ALIGN_PARAGRAPH.CENTER,
          "left": WD_ALIGN_PARAGRAPH.LEFT, "right": WD_ALIGN_PARAGRAPH.RIGHT}[align]
    p.alignment = al
    pf = p.paragraph_format
    pf.line_spacing = spacing
    pf.space_before = Pt(before)
    pf.space_after = Pt(after)
    if indent:
        pf.first_line_indent = Cm(1.25)
    if text:
        _runs(p, text, bold=bold, italic=italic, size=size)
    return p


def lead(label, rest, size=SZ):
    """Жоон баштапкы сөз айкашы + кадимки текст (КИРИШҮҮ бөлүмдөрү үчүн)."""
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
    pf = p.paragraph_format
    pf.line_spacing = 1.5
    pf.first_line_indent = Cm(1.25)
    pf.space_after = Pt(6)
    _runs(p, label, bold=True, size=size)
    _runs(p, rest, bold=False, size=size)
    return p


def bullet(text, size=SZ):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
    pf = p.paragraph_format
    pf.line_spacing = 1.5
    pf.first_line_indent = Cm(1.25)
    pf.space_after = Pt(3)
    _runs(p, "— " + text, size=size)
    return p


def chapter(title):
    para(title, bold=True, align="center", indent=False, before=12, after=6)


def subsection(title):
    para(title, bold=True, align="center", indent=False, before=10, after=6)


def pagebreak():
    doc.add_page_break()


def toc_line(text, page):
    """МАЗМУНУ сабы: текст ......... бет (чекит-лидерлер таб менен)."""
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
    pf = p.paragraph_format
    pf.line_spacing = 1.5
    pf.space_after = Pt(4)
    # оң жакта табуляция + чекит-лидер
    tab = pf.tab_stops
    tab.add_tab_stop(Cm(16), alignment=2, leader=2)  # 2=RIGHT, 2=DOT
    bold = text.endswith("*")
    txt = text.rstrip("*")
    _runs(p, txt, bold=bold)
    _runs(p, "\t" + str(page), bold=bold)


